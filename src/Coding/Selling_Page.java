    private void CalculatePrice()
    {
        /* 
            select price from Product_table that admin entered.
            price will be calculated with the selected flavour. 
        */
        String flavour = cmbflavourname.getSelectedItem().toString();
        int quantity = Integer.parseInt(tbxquantity.getText());
        float bill =0;
        try
        {
           
            db.DbConnect();
            String sql = "Select price from Product_table where Flavour_name = ?";
            db.pstatement = db.con.prepareStatement(sql);
            db.pstatement.setString(1, flavour);
            db.rs = db.pstatement.executeQuery();
            if(db.rs.next())
            {
                int price = db.rs.getInt("price");          // calculating price for the selected combo item from the database
                bill = (price*quantity);
            }
            String Tprice = String.valueOf(bill);
            tbxtotalprice.setText(Tprice);

        } catch (NumberFormatException | SQLException e) 
        {
            System.out.print(e);
        }
        
        /*
        
            after calculating price of the selected flavour; quantity , flavour name, and total price will be
            saved in the database.

        */
          int confirm = JOptionPane.showConfirmDialog(null, "Do you want the selected item?", "Confirmation message", JOptionPane.YES_NO_OPTION);
        if(confirm == 0)
        {
        
        try 
        {
            String id = Stuff_login.StuffID;
            db.DbConnect();
            String Sql = "Insert into Selling_table (Flavour_name, Quantity, Bill) values (?,?,?)";
            db.pstatement = db.con.prepareStatement(Sql);
            
            db.pstatement.setString(1, flavour);
            db.pstatement.setInt(2, quantity);
            db.pstatement.setFloat(3, bill);
            
            db.pstatement.executeUpdate();
            tbxquantity.setText("");
            
        } catch (SQLException e) 
        {
            System.out.print(e);
            //JOptionPane.showMessageDialog(null, e);
        }
        }
        else
        {
            tbxquantity.setText("");
            tbxtotalprice.setText("");
        }
        
    }
    
    
