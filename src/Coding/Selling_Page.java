    private void ShowFlavourinCombobox()
    {
        /*
        showing flavour name in the combo box from the database from Product_table 
        that admin inserted.
        */
        try 
        {   
            db.DbConnect();
            
            String sql = "Select Flavour_name from Product_table";
            db.pstatement = db.con.prepareStatement(sql);
            db.rs =  db.pstatement.executeQuery();
            
            while(db.rs.next())
            {
                String F_name = db.rs.getString("Flavour_name");
                cmbflavourname.addItem(F_name);
            }
            
        } catch (SQLException e) 
        {
            System.out.print(e);
        }
    }
    
    private void showFlavourinfoInTable()
    {
        
        /*
         adding flavour_name and price in the table
        */
        try 
        {
             db.DbConnect();
        
            String sql = "Select * from Product_table";
            db.pstatement = db.con.prepareStatement(sql);
            db.rs = db.pstatement.executeQuery();
            DefaultTableModel table = (DefaultTableModel) tblsellingtable.getModel();
            
            while(db.rs.next())
            {
                String F_name = db.rs.getString("Flavour_name");
                String F_price = db.rs.getString("price");
            
            table.addRow(new Object[]
            {
                F_name, F_price
            });
            }
        
        } catch (SQLException e) 
        {
            System.out.print(e);
        }
    }
