    private void show_stuff_info()
    {
       
        try 
        {
            DatabaseConnection db = new DatabaseConnection();
            db.DbConnect();
            String sql = "select Stuff_id, stuff_name, stuff_email, stuff_birthday, stuff_nationalid,stuff_phonenumber, stuff_address from Stuff_info where Stuff_id ="+id+"";
            db.pstatement = db.con.prepareStatement(sql);
            db.rs = db.pstatement.executeQuery();
            
            if(db.rs.next())
            {
                String s_id = db.rs.getString("Stuff_id");
                String s_name = db.rs.getString("stuff_name");
                String s_email = db.rs.getString("stuff_email");
                String s_birthday = db.rs.getString("stuff_birthday");
                String s_nationalid = db.rs.getString("stuff_nationalid");
                String s_phone = db.rs.getString("stuff_phonenumber");
                String s_address = db.rs.getString("stuff_address");
                
                tbxstuffid.setText(s_id);
                tbxstuffname.setText(s_name);
                tbxstuffemail.setText(s_email);
                tbxstuffbirthday.setText(s_birthday);
                tbxstuffnationalid.setText(s_nationalid);
                tbxstuffphonenumber.setText(s_phone);
                tbxstuffaddress.setText(s_address);
            }
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
