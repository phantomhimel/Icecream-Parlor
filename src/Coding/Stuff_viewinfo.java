    private void Update_info()
    {
         try 
        {
            DatabaseConnection db = new DatabaseConnection();
            db.DbConnect();
            String sql = "Update Stuff_info set stuff_name =?, stuff_nationalid =?, stuff_phonenumber =?, stuff_address =? where Stuff_id ="+id+"";
            db.pstatement = db.con.prepareStatement(sql);
            db.pstatement.setString(1, tbxstuffname.getText());
            db.pstatement.setString(2, tbxstuffnationalid.getText());
            db.pstatement.setString(3, tbxstuffphonenumber.getText());
            db.pstatement.setString(4, tbxstuffaddress.getText());
            db.pstatement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Infomation Updated Successfully ");
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
