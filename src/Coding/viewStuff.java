    private void StuffInfo()
    {
        try 
        {
            DatabaseConnection db = new DatabaseConnection();
            db.DbConnect();
            String sql = "Select * from Stuff_info";
            db.pstatement = db.con.prepareStatement(sql);
            db.rs = db.pstatement.executeQuery();
            DefaultTableModel table = (DefaultTableModel) tblviewstuff.getModel();
            while(db.rs.next())
            {
                table.addRow(new Object[]
                {
                    db.rs.getInt("Stuff_id"), db.rs.getString("stuff_name"),
                    db.rs.getString("Stuff_email"), db.rs.getString("stuff_password"), db.rs.getString("stuff_birthday"),
                    db.rs.getString("Stuff_nationalid"), db.rs.getString("stuff_phonenumber"),
                    db.rs.getString("Stuff_address")
                });
            }
        } catch (SQLException e)
        {
            System.out.print(e);
        }
        
    }
    
