    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        // TODO add your handling code here:
        
        int confirm = JOptionPane.showConfirmDialog(null, "Do you really want to delete the selected item?", "Confirmation message", JOptionPane.YES_NO_OPTION);
        if(confirm == 0)
        {
        try 
        {
            
            DatabaseConnection db = new DatabaseConnection();
            db.DbConnect();
            DefaultTableModel table = (DefaultTableModel) tblviewstuff.getModel();
            int selected_row = tblviewstuff.getSelectedRow();
            String row_value = tblviewstuff.getModel().getValueAt(selected_row, 0).toString();
            String sql = "Delete from Stuff_info where stuff_id = "+row_value;
            db.pstatement = db.con.prepareStatement(sql);
            db.pstatement.executeUpdate();
            table.removeRow(selected_row);
            JOptionPane.showMessageDialog(null, "Deleted Info Successfully");
            
        } catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null, "Data not deleted. Please select a row and try again.", "Message", JOptionPane.WARNING_MESSAGE);
        }
        }
        
    }//GEN-LAST:event_jLabel7MouseClicked

